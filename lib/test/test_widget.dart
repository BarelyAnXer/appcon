                  focusNode: _model.textFieldFocusNode,
                  onChanged: (_) => EasyDebounce.debounce(
                    '_model.textController',
                    const Duration(milliseconds: 500),
                    () async {
                      setState(() {
                        FFAppState().updateAnswerHealthAssessmentAtIndex(
                          0,
                          (_) => <String, dynamic>{
